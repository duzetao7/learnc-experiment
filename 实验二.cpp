#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
int main()
{
	int a, b;
	float c, d;
	long e, f;
	unsigned int u, v;
	char c1, c2;
	scanf("%d,%d", &a, &b);
	scanf("%f,%f", &c, &d);
	scanf("%d,%d", &e, &f);
	scanf("%d,%d", &u, &v);
	getchar();
	scanf("%c,%c", &c1, &c2);
	printf("\n");
	printf("a = %7d, b = %7d\n", a, b);
	printf("c = %10.2f, d = %10.2f\n", c, d);
	printf("e = %17d, f = %17d\n", e, f);
	printf("u = %d, v = %d\n", u, v);
	printf("c1 = %c, c2 = %c\n", c1, c2);
	return 0;
}